<p>　　根据数据统计，德林克沃特此役触球116次，是全场触球最多的球员，105次传球93次成功。而在防守端，他的表现同样十分亮眼，完成了1次拦截和4次成功的铲抢。whoscored的网站为德林克沃特赛后打出了8.5的全场并列第3的高分，仅次于威廉和佩德罗。</p> 
<p>　　德林克沃特此役在中场的表现如有神助，这不禁令人想起了切尔西另一位中场新援巴卡约科。法国人如今的处境比较尴尬，他在防守时常常在关键位置漏人，导致球队陷于被动，而在进攻时，他与队友之间缺少默契，多次成为了蓝军进攻的终结者，不少球迷也在呼吁蓝军应该停止使用巴卡约科。而在德林克沃特出场之后，蓝军中卫线得到了有效的保障，且进攻时多了一位出色的传球手，可以说切尔西阵容的最大短板得到了很好的弥补。</p> 
<p>　　如今看来，用德林克沃特取代巴卡约科首发，或许是切尔西做出的最正确的决策。在马蒂奇今夏离开切尔西之后，人们始终在讨论他的替身巴卡约科，很多人甚至忽略了德林克沃特的存在，而他在中场稳健的表现正是切尔西急需的，没准德林克沃特才是切尔西苦苦寻找的马蒂奇接班人。</p> 
<p>　　孔蒂如今或许该考虑将巴卡约科从首发阵容拿下，多给喝水哥适应蓝军足球的机会。毕竟，切尔西下轮将挑战阿森纳，欧冠又要与巴塞罗那扳扳手腕儿，强强对话容不得半点致命的失误。</p> 
<p>　　（白鸟）</p> 

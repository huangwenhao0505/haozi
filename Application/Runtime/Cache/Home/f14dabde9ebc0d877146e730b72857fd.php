 <img src="http://n.sinaimg.cn/sports/transform/w479h225/20171231/Llat-fyqefvw4803392.gif" alt="喝水哥神球" data-link="">
 <span class="img_descr">喝水哥神球</span>
</div> 
<p>　　比赛第9分钟，德林克沃特成为了场上主角，佩德罗传球被防守球员挡出后，喝水哥大禁区前沿停球、右脚抽射一气呵成，没有给周围的防守球员任何封堵的机会，而皮球在空中画出了一道美妙的抛物线后飞入球门远端死角。面对这粒约23米远的精彩世界波，英格兰国门巴特兰德呆若木鸡，目送皮球入网。</p> 
<p>　　BBC对于德林克沃特精彩的进球赞不绝口：“多么美妙的进球。大家可以回家休息去了，比赛已经结束了。”值得一提的是，这粒进球也是德林克沃特加盟切尔西后的首粒联赛进球，以如此精彩的世界波开启自己在蓝军的进球账户，对于喝水哥来说无疑相当美妙。一位国外的网友甚至调侃道：“多么精彩的进球，来喝点水吧，丹尼。”</p> 
<p>　　随后，德林克沃特愈战愈勇，展现了自己良好的中场梳理能力，而他还为队友送出了多次精准的长传。上半场补时阶段，喝水哥中圈附近长传转移找到了高速插至禁区左侧的阿隆索，可惜后者没有第一时间选择抽射，横敲时被对手破坏。第52分钟，又是德林克沃特的长传，这一次他准确的找到了反越位成功的莫拉塔，可惜后者在禁区内处理球时被防守球员解围。此外，喝水哥的出色的防守功底也得到了展现，第49分钟，在防守索比突破时，他牢牢卡住身位，出脚快速准确，将皮球干净利落的断下，发动了一次反击。</p> 

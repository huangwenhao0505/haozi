<p>　　不鸣则已，一鸣惊人！</p> 
<p>　　用这句话来形容切尔西中场德林克沃特再合适不过了。这位以3500万英镑加盟切尔西的英格兰国脚在赛季初期受到了伤病的困扰，不过随着伤势的恢复，德林克沃特已经逐渐找回了自己的最佳状态，并在与斯托克城的比赛中踢出了加盟蓝军后最高光的一场比赛，他也向外界证明自己有成为球队大脑的潜质。</p> 
<p>　　由于连续的一周双赛，孔蒂对阵容进行了多处调整，德林克沃特则迎来了自己联赛第3次首发机会。喝水哥此役在中场表现的游刃有余，进可攻退可守，而他精准的长传制导更是令对手防不胜防。</p> 
